package dailyVideos.day04_concatenation;

public class Concatenation_addition {

    public static void main(String[] args) {

        System.out.println("32"+1); //concatenation output= 321
        System.out.println(1+"32");// concatenation output= 132


        System.out.println("--------------------------------------");

        System.out.println(12+57); // addition output=69




    }
}
