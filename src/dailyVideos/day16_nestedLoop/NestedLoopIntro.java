package dailyVideos.day16_nestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {


        // I do not have to repeat them myself

        for (int i = 0; i <5 ; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i <5 ; i++) {
            System.out.println("Wooden Spoon");

        }


        for (int i = 0; i <5 ; i++) {
            System.out.println("Wooden Spoon");

        }
        System.out.println("-------------------------------------");

        //so we use nested loop

        for (int i = 0; i < 5; i++) {

            for (int j = 0; j <5 ; j++) {
                System.out.println("Wooden Spoon");
            }
        }




    }
}
